# show "I love you" in the window of that red center heart. Using tuple.
import turtle

turtle.color("red")
turtle.begin_fill()
turtle.left(50)
turtle.forward(133)
turtle.circle(50, 200)
turtle.right(140)
turtle.circle(50, 200)
turtle.forward(133)
turtle.end_fill()
turtle.color("yellow")
turtle.hideturtle()
turtle.done()
